// lib/categoryRules.ts

const rules: Record<string, string> = {
  'من أنا': `
    - كل سؤال يبدأ حرفياً بـ "أنا..."
    - يصف شخصية أو كياناً بأسلوب تلميحي
    - ممنوع ذكر الاسم في السؤال
    - easy: تلميح واضح جداً
    - hard: تلميح نادر يتطلب معرفة عميقة`,

  'صح أو خطأ': `
    - كل سؤال جملة خبرية يجب الحكم عليها
    - الإجابة دائماً: "صح" أو "خطأ" فقط
    - medium: جمل تبدو صحيحة لكنها خاطئة أو العكس`,

  'أكمل الجملة': `
    - كل سؤال عبارة منقوصة تنتهي بـ "..."
    - الإجابة هي الكلمة أو الجملة المفقودة فقط
    - easy: أمثال أو جمل شهيرة جداً`,

  'كرة القدم': `
    - أسئلة عن اللاعبين، الأندية، البطولات، الأرقام القياسية
    - easy: أسماء لاعبين مشهورين أو بطولات كبرى
    - hard: أرقام دقيقة، أهداف تاريخية، تفاصيل نادرة`,

  'جغرافيا': `
    - أسئلة عن مواقع، دول، عواصم، حدود، ظواهر طبيعية
    - easy: عواصم دول كبرى أو قارات
    - hard: مساحات دقيقة، حدود نزاع، إحصائيات جغرافية`
}

export function getSpecialRules(categoryName: string, topicName?: string): string {
  if (rules[categoryName]) return rules[categoryName]

  const filmKeywords = ['فيلم', 'أفلام', 'مسلسل', 'أنمي', 'سينما', 'ألعاب']
  if (filmKeywords.some(k => categoryName.includes(k))) {
    return `
      - الأسئلة مرتبطة حصرياً بـ: ${topicName || categoryName}
      - تشمل: أحداث القصة، الشخصيات، الحوارات، التفاصيل الدقيقة
      - ممنوع الخروج عن عالم هذا العمل تحديداً`
  }

  return `
    - عامل هذه الفئة كعالم مغلق: ${categoryName}${topicName ? ` — ${topicName}` : ''}
    - الأسئلة لا تخرج عن هذا الموضوع أبداً
    - استنتج السياق من اسم الفئة وأنتج أسئلة ملائمة`
}
